    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int confirmation=JOptionPane.showConfirmDialog(this,"Are you sure you want to delete all received messages? This action cannot be undone.\nWARNING: THIS ACTION WILL ALSO REMOVE THE MESSAGE FROM THE SENDER'S SENTBOX","Clear received messages",JOptionPane.YES_NO_OPTION);
        if(confirmation==0) {
            try {
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost/project","root","");
                Statement stmt=con.createStatement();
                String query="DELETE FROM messages WHERE receiver=\'"+username+"\';";
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(this,"Deleted all received messages");
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }            
    }//GEN-LAST:event_jButton2ActionPerformed

