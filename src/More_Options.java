    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int confirmation=JOptionPane.showConfirmDialog(this,"Are you sure you want to delete your account? This action cannot be undone.\nWARNING: THIS ACTION WILL ALSO REMOVE ALL MESSAGES FROM YOUR INBOX AND SENTBOX","Remove account",JOptionPane.YES_NO_OPTION);
        if(confirmation==0) {
            try {
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost/project","root","");
                Statement stmt=con.createStatement();
                String query1="DELETE FROM credentials WHERE username=\'"+username+"\';";
                String query2="DELETE FROM messages WHERE sender=\'"+username+"\' OR receiver=\'"+username+"\';";
                stmt.executeUpdate(query1);
                stmt.executeUpdate(query2);
                JOptionPane.showMessageDialog(this,"Account has been removed");
                
                Login_Form Login=new Login_Form();                              // Going back to 'Login_Form.java'.
                Login.setVisible(true);
                this.dispose();
            }
            catch(Exception e) {
                JOptionPane.showMessageDialog(this,e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

